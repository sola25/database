package express;

public class Express {
private String ExpId;
private String OrdId;
private String ExpInf;
private String ExpCourId;
private String ExpCourName;
private String ExpCourTele;




public String getExpId() {
	return ExpId;
}

public void setExpId(String expId) {
	ExpId = expId;
}

public String getOrdId() {
	return OrdId;
}

public void setOrdId(String ordId) {
	OrdId = ordId;
}

public String getExpInf() {
	return ExpInf;
}

public void setExpInf(String expInf) {
	ExpInf = expInf;
}

public String getExpCour() {
	return ExpCourId;
}

public void setExpCour(String expCour) {
	ExpCourId = expCour;
}

public String getExpCourId() {
	return ExpCourId;
}

public void setExpCourId(String expCourId) {
	ExpCourId = expCourId;
}

public String getExpCourName() {
	return ExpCourName;
}

public void setExpCourName(String expCourName) {
	ExpCourName = expCourName;
}

public String getExpCourTele() {
	return ExpCourTele;
}

public void setExpCourTele(String expCourTele) {
	ExpCourTele = expCourTele;
}

}
